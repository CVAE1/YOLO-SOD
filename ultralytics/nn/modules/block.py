class GroupBatchnorm2d(nn.Module):
    def __init__(self, c_num:int, group_num:int = 16, eps:float = 1e-10):
        super(GroupBatchnorm2d,self).__init__()  # è°ƒç”¨çˆ¶ç±»æž„é€ å‡½æ•°
        assert c_num >= group_num  # æ–­è¨€ c_num å¤§äºŽç­‰äºŽ group_num
        self.group_num  = group_num  # è®¾ç½®åˆ†ç»„æ•°é‡
        self.gamma      = nn.Parameter(torch.randn(c_num, 1, 1))  # åˆ›å»ºå¯è®­ç»ƒå‚æ•° gamma
        self.beta       = nn.Parameter(torch.zeros(c_num, 1, 1))  # åˆ›å»ºå¯è®­ç»ƒå‚æ•° beta
        self.eps        = eps  # è®¾ç½®å°çš„å¸¸æ•° eps ç”¨äºŽç¨³å®šè®¡ç®—
 
